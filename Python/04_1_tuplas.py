"""
Una tupla es una estructura de datos inmutable y ordenada que 
permite almacenar una colección de elementos. Los elementos 
de una tupla se encierran entre paréntesis (), separados por
comas.
"""
punto = (3, 4)

"""
Para acceder a los elementos de una tupla, utiliza el índice 
del elemento entre corchetes, similar a las listas.
"""

print(punto[0])
print(punto[1])